namespace TheSlimeKing.Gameplay.Stealth
{
    /// <summary>
    /// Define os tipos de objetos que podem ser usados como cobertura
    /// </summary>
    public enum CoverType
    {
        /// <summary>
        /// Vegetação rasteira, como grama alta
        /// </summary>
        Grass,

        /// <summary>
        /// Arbustos e vegetação média
        /// </summary>
        Bush,

        /// <summary>
        /// Pedras e estruturas rochosas
        /// </summary>
        Rock,

        /// <summary>
        /// Árvores e vegetação grande
        /// </summary>
        Tree
    }
}
